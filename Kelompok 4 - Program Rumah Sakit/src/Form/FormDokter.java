    private void TxtKdDokterCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_TxtKdDokterCaretUpdate
        // TODO add your handling code here:
        try {
            Connection c=Rumahsakit.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select * from form_dokter where kode_dokter='" + this.TxtKdDokter.getText() + "'";
            ResultSet r=s.executeQuery(sql);
            while (r.next()){
                this.TxtNama.setText(r.getString(3));
                if ("Laki-laki".equalsIgnoreCase(r.getString("jk"))){
                    RbLakilaki.setSelected(true);
                } else {
                    RbPerempuan.setSelected(true);
                }
                this.TxtAlamat.setText(r.getString("alamat"));
                this.TxtTlp.setText(r.getString("no_tlp"));
                this.TxtSpesialis.setText(r.getString("spesialis"));
            }
            r.close();
            s.close();
            
        }catch(SQLException e) {
            System.out.println("Terjadi Kesalahan" + e);
        }
    }//GEN-LAST:event_TxtKdDokterCaretUpdate

