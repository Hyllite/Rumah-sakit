    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        // TODO add your handling code here:
        String kode_dokter=this.TxtKdDokter.getText();
        String nama=this.TxtNama.getText();
        String jk = null;
        if (RbLakilaki.isSelected()){
            jk = "Laki-Laki";
        }else if(RbPerempuan.isSelected()){
            jk = "Perempuan";
        }
        String alamat=this.TxtAlamat.getText();
        String no_tlp=this.TxtTlp.getText();
        String spesialis=this.TxtSpesialis.getText();
        if (TxtKdDokter.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Kode Dokter Tidak Boleh Kosong");
        }else {
            if(TxtNama.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Nama Dokter Tidak boleh Kosong");
            }else {
                if(TxtAlamat.getText().isEmpty()){
                    JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Alamat Tidak Boleh Kosong");
                }else {
                    if(TxtTlp.getText().isEmpty()){
                        JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Nomor Tlp tidak Boleh Kosong");
                    }else {
                        if(TxtSpesialis.getText().isEmpty()){
                            JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Spesialis tidak Boleh Kosong");
                        }else {
                        try {
                            Connection c=Rumahsakit.getkoneksi();
                            String sql = "Insert into form_dokter values (?,?,?,?,?,?)";
                            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
                            p.setString(1, kode_dokter);
                            p.setString(2, nama);
                            p.setString(3, alamat);
                            p.setString(4, no_tlp);
                            p.setString(5, jk);
                            p.setString(6, spesialis);
                            p.executeUpdate();
                            p.close();
                            JOptionPane.showMessageDialog(this, "Sukses Tambah Data");
                        }catch(SQLException e){
                            System.out.println(e);                            
                        }finally{
                        }
                    }
                }
            }
        }try {
            Connection c=Rumahsakit.getkoneksi();
            Statement s= c.createStatement();
            String sql="select kode_dokter, nama_dokter, alamat, nohp, jk, sp from form_dokter";
            ResultSet r=s.executeQuery(sql);
            TblDokter.setModel(DbUtils.resultSetToTableModel(r));
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_BtnSaveActionPerformed

